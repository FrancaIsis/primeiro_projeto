import 'dart:io';

void main() {
  //Lista do tipo STRING
  List<String> panteras76 = ['Kelly Garret', 'Jill Munroe', 'Sabrina Duncan'];

  for (String personagem in panteras76) {
    print(personagem);
  }
}
// Neste exemplo, a lista de strings panteras76 é inicializada com 3 elementos.
// Em seguida, o "for-in" é usado para iterar sobre cada elemento da lista
// e imprimir na saída padrão.
// A varável personagem  é declarada como do tipo String e receberá cada elemento da
// lista em cada iteração.